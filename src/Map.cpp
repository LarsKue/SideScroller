Map::Map() {
    dirtTex = new TextureManager("assets/dirt.png");
    grassTex = new TextureManager("assets/grass.png");
    waterTex = new TextureManager("assets/HD_water.png");

    LoadMap(lvl1);

    src.x = 0;
    src.y = 0;
    src.w = tex_size;
    src.h = tex_size;

    dest.x = 0;
    dest.y = 0;
    dest.w = tex_size;
    dest.h = tex_size;
}

void Map::LoadMap(int arr[20][25]) {
    for(int row = 0; row < 20; row++) {
        for(int column = 0; column < 25; column++) {
            map[row][column] = arr[row][column];
        }
    }
}

void Map::DrawMap() {
    int type = 0;

    for(int row = 0; row < 20; row++) {
        for(int column = 0; column < 25; column++) {
            type = map[row][column];

            dest.x = column * tex_size;
            dest.y = row * tex_size;

            switch(type) {
                case 0:
                    waterTex->Draw(src, dest);
                    break;
                case 1:
                    grassTex->Draw(src, dest);
                    break;
                case 2:
                    dirtTex->Draw(src, dest);
                    break;
                default:
                    break;
            }
        }
    }
}

